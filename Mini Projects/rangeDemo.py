""" rangeDemo.py
    demonstrates how range object creates automatic lists of numbers
    17 Jan, 2021"""

print("range(3)")
print(range(3))
print()

print("range(1, 3)")
print(range(1, 3))
print()

print("range(2, 5)")
print(range(2, 5))
print()

print("range(0, 55, 5)")
print(range(0, 55, 5))
print()

print("range(50, 0, -5)")
print(range(50, 0, -5))
print()
